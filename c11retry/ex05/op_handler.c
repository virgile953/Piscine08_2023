#include "lib.h"

int	ft_multiply(int a, int b)
{
	return (a * b);
}

int	ft_add(int a, int b)
{
	return (a + b);
}

int	ft_subst(int a, int b)
{
	return (a - b);
}

int	ft_mod(int a, int b)
{
	return (a % b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}
